package hw14dk;

public class Classd1 {
    private String name;
    private Boolean rgb;

    public Classd1(String name, Boolean rgb) {
        this.name = name;
        this.rgb = rgb;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getRgb() {
        return rgb;
    }

    public void setRgb(Boolean rgb) {
        this.rgb = rgb;
    }
}
